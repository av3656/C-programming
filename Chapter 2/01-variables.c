#include <stdio.h>
int main()
{
    //%d is a format specifier.
    //%d is for integers.
    //%f is for float(Decimals)
    //%c is for char(characters)
    int a = 5;
    int b = a;
    int c = 6, d = 45, e = 445, f = 244;
    printf("The value of a is %d and the value of b is %d\n.", a, b);
    printf("The value of c is %d and the value of d is %d\n.", c, d);
    printf("The value of e is %d and the value of f is %d\n.", e, f);

    return 0;
}